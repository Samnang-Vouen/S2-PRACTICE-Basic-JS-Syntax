function reverseArray(array) {
    return array.reverse();
}

console.log("array = [14,15,16,20]");
console.log("Reverse an array:", reverseArray([14, 15, 16, 20]));

console.log("array = [5,4,3,2,1]");
console.log("Reverse an array:", reverseArray([5, 4, 3, 2, 1]));

console.log("array = []");
console.log("Reverse an array:", reverseArray([])); 
